def sum_value(operants_1, operants_2):
    '''
    СЛОЖЕНИЕ
    '''
    sum_res = operants_1 + operants_2
    return sum_res


def sub_value(operants_1, operants_2):
    '''
    ВЫЧИТАНИЕ
    '''
    sub_res = operants_1 - operants_2
    return sub_res


def del_value(operants_1, operants_2):
    '''
    ДЕЛЕНИЕ
    '''
    del_res = operants_1 / operants_2
    return del_res


def mult_value(operants_1, operants_2):
    '''
    УМНОЖЕНИЕ
    '''
    mult_res = operants_1 * operants_2
    return mult_res
